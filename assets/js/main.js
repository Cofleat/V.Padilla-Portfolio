function listTags(items) {
  return items.map((item) => `<span class="tag">${item}</span>`).join("");
}

function renderSkillCard(item, className) {
  return `
    <article class="${className} reveal">
      <span class="icon-chip" aria-hidden="true">${item.icon}</span>
      <h3>${item.title}</h3>
      <div class="tag-list">${listTags(item.items)}</div>
    </article>
  `;
}

function renderProjectImage(src, alt) {
  if (/\.svg$/i.test(src)) {
    return `
      <picture>
        <source srcset="${src}" type="image/svg+xml">
        <img src="${src.replace(/\.svg$/i, ".png")}" alt="${alt}" loading="lazy">
      </picture>
    `;
  }

  return `<img src="${src}" alt="${alt}" loading="lazy">`;
}

function slugify(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function certificationFolder(title) {
  return `assets/certificates/${slugify(title)}`;
}

function certificationFileLink(item) {
  const fileName = item.fileName || "certificate.pdf";
  return `${certificationFolder(item.title)}/${fileName}`;
}

